def additional_checks_for(business_type: str) -> list[str]:
    text = (business_type or "").lower()
    for keywords, checks in _CHECKS_BY_KEYWORD:
        if any(keyword in text for keyword in keywords):
            return checks
    return list(_DEFAULT_CHECKS)


def format_additional_checks_block(business_type: str) -> str:
    checks = additional_checks_for(business_type)
    lines = ["\n<b>Что требуется проверить дополнительно:</b>"]
    for index, item in enumerate(checks, start=1):
        lines.append(f"{index}. {item}")
    return "\n".join(lines)
