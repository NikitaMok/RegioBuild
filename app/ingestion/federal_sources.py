def write_curated_jsonl(out_dir: Path | None = None) -> Path:
    """Пишет curated JSONL для последующей загрузки в индекс."""
    target = out_dir or CURATED_DIR
    target.mkdir(parents=True, exist_ok=True)
    out_path = target / "federal_extra.jsonl"
    with out_path.open("w", encoding="utf-8") as handle:
        for chunk in all_curated_chunks():
            handle.write(
                json.dumps(
                    {
                        "region_code": chunk.region_code,
                        "section_number": chunk.section_number,
                        "text": chunk.text,
                        "source_label": chunk.source_label,
                        "category": chunk.category,
                        "business_types": list(chunk.business_types),
                    },
                    ensure_ascii=False,
                )
                + "\n"
            )
    return out_path
